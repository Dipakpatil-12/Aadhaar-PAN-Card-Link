package Spark_2_0_Java_Full_Stack_.oops;

public class OverloadingDemo {

            public int add(int a,int b)
            {
                return a+b;
            }
            public int add(int a,int b,int c)
            {
                return a+b+c;
            }

            public int add(int a,int b,int c,int d)
            {
                return a+b+c+d;
            }
        }


